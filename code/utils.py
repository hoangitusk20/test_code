def prepare_folder(args):
    ''' Create log and model folder'''
    folder_utils = [args.root_log, args.root_model,
                    os.path.join(args.root_log,args.store_name),
                    os.path.join(args.root_model,args.store_name)]
    
    for folder in folder_utils:
        if not os.path.exists(folder):
            print('creating folder' + folder)
            os.mkdir(folder)

def save_checkpoint(args, state, is_best, filename=''):
    if not filename:
        filename = '%s/%s/ckpt.pth.tar' % (args.root_model, args.store_name)

    torch.save(state,filename)
    if is_best:
        shutil.copyfile(filename,filename.replace('pth.tar','best.pth,tar'))

class AverageMeter(object):
    """Compute and  stores the average and current value"""
    def __init__(self,name,fmt =":f"):
        self.fmt = fmt
        self.name = name
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.count = 0
        self.sum = 0

    def update(self, val, n = 1):
        self.val = val
        self.sum += val * n
        self.count +=n
        self.avg = self.sum / self.count

    def __str__(self):
        fmtstr = '{name} {val' + self.fmt + '} ({avg' + self.fmt + '})'
        return fmtstr.format(**self.__dict__)

class ProgressMetter(object):
    def __init__(self,num_batches, *meters, prefix=""):
        self.batch_fmtstr = self.get_batch_fmtstr(num_batches)
        self.meters = meters
        self.prefix = prefix

    def print(self, batch):
        entries = [self.prefix + self.batch_fmtstr.format(batch)]
        entries += [str(meter) for meter in self.meters]
        print('\t'.join(entries))
    
    def get_batch_fmtstr(self, num_batches):
        num_digits = len(str(num_batches // 1))
        fmt = '{:' +str(num_digits) + 'd}'
        return '[' + fmt + '/' + fmt.format(num_batches) + ']'
    
def accuracy(output,target, topk = (1,)):
    """Computes the accuracy over the k top predictions for the specified values of k"""

    ###########
    with torch.no_grad():
        maxk = max(topk)
        batch_size = target.size(0)

    ##########
        _,pred = output.topk(maxk, 1, True, True)
        pred = pred.t()
        correct = pred.eq(target.reshape(1,-1).expand_as(pred))

        res = []
        for k in topk:
            correct_k = correct[:k].reshape(-1).float().sum(0,keepdim = True)
            res.append(correct_k.mul_(100.0 / batch_size))

    return res